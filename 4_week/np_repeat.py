# картинка 28x28 → вектор из 784 чисел
img = np.zeros((28, 28))      # одна картинка
img_flat = img.reshape(784)   # разворачиваем
print(img_flat.shape)         # (784,)
# получили картинку в виде вектора, который можно подать на вход нейросети - вектор из 784 чисел, который описывает яркость каждого пикселя

rng = np.random.default_rng(seed=42)

# W — матрица весов. 784 входа → 256 нейронов (выбрали произвольное число). 256 столбцов = 256 нейронов, 784 строки = 784 веса на вход каждого нейрона
# каждая строка = один нейрон = 784 веса
W = rng.standard_normal((784, 256))
print(W.shape)  # (784, 256)

# один пример через слой
x = rng.random(784)   # один вектор-картинка (рандомные числа от 0 до 1 для примера)
y = x @ W             # умножение — получаем 256 чисел (это активации 256 нейронов)
print(y.shape)         # (256,)

# не одна картинка, а сразу 32
batch = rng.random((32, 784))   # 32 картинки, каждая 784 числа
out = batch @ W                  # все 32 за один раз
print(out.shape)                 # (32, 256)

# ----------- теперь инициализация целиком
n_in = 784  # сколько входов в слой

# без коэффициента — числа слишком большие
W_bad = rng.standard_normal((784, 256))
print(W_bad.std().round(3))     # ~1.0 — слишком много

# He-инициализация — умножаем на маленький коэффициент
W_good = rng.standard_normal((784, 256)) * np.sqrt(2 / n_in)
print(W_good.std().round(3))    # ~0.05 — в самый раз

# ------------ Dropout целиком
x = rng.standard_normal((32, 256))  # батч после первого слоя
print(x.mean().round(3))             # среднее ~0

# создаём маску: случайные True/False
mask = rng.random((32, 256)) > 0.5
print(mask[0, :5])   # [True False True True False] — примерно половина

# выключаем нейроны
x_dropped = x * mask
print(x_dropped.mean().round(3))    # среднее стало ~вдвое меньше

# восстанавливаем масштаб
x_scaled = x_dropped / 0.5
print(x_scaled.mean().round(3))     # вернулось к исходному масштабу

'''
Картинка 784 пикселя и 256 нейронов — матрица 784×256. То есть каждый из 256 нейронов имеет свои 784 веса. Итого 784×256 = 200 704 весов в одном слое.
32 картинки × матрица весов — верно.
W сейчас случайная, она ни на чём не училась. Это просто инициализация до обучения. После обучения — да, W будет содержать выученные паттерны и умножение покажет "насколько картинка похожа на то что сеть знает".

---

Зачем много слоёв?

Каждый слой учится видеть что-то своё, более сложное чем предыдущий.
Первый слой видит края и градиенты — просто перепады яркости пикселей.
Второй слой берёт эти края и собирает из них формы — уголки, дуги.
Третий слой из форм собирает части объектов — глаз, колесо, цифра "8".
Четвёртый и дальше — целые объекты и их соотношения.

Один слой так не может, потому что `x @ W` — это линейное преобразование. Два линейных преобразования подряд математически схлопываются в одно.
Поэтому между слоями добавляют нелинейность — функцию активации (ReLU и др.). Она ломает линейность и позволяет каждому слою учить что-то принципиально новое.
'''

# без нелинейности два слоя = один слой (бессмысленно)
W1 = rng.standard_normal((784, 256)) * np.sqrt(2/784)
W2 = rng.standard_normal((256, 128)) * np.sqrt(2/256)

x = rng.random((32, 784))

# плохо — линейность сохраняется
out = x @ W1 @ W2

# правильно — ReLU между слоями
layer1 = x @ W1
layer1_relu = np.maximum(0, layer1)   # ReLU: отрицательное → 0
layer2 = layer1_relu @ W2
print(layer2.shape)  # (32, 128)

