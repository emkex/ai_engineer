'''
Почему это важно для ИИ:
np.zeros / ones — инициализация весов нейросети. Все нули = мёртвая сеть (градиенты одинаковые). Поэтому на практике используют случайную инициализацию (Xavier, He).
np.eye — единичная матрица это нейтральный элемент умножения. В трансформерах начальные веса attention иногда инициализируют близко к единичной. В residual-сетях (ResNet) идея та же: выход блока = вход + небольшая поправка, то есть слой учится отклонению от I.
np.logspace — learning rate подбирают на логшкале: 1e-5, 1e-4, 1e-3. На линейной шкале большие значения доминируют, на логарифмической всё равномерно.
dtype=float32 — в ML почти всегда float32, не float64. Вдвое меньше памяти, GPU оптимизированы под f32. float64 нужен только для численных методов где важна точность (химия, физика).
'''

